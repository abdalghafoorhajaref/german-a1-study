let currentLang = 'en';

function detectDeviceLanguage() {
  const browserLang = navigator.language || navigator.userLanguage || 'en';
  const cleanLang = browserLang.toLowerCase().slice(0, 2);
  if (cleanLang === 'tr') return 'tr';
  if (cleanLang === 'ar') return 'ar';
  if (cleanLang === 'ro') return 'ro';
  if (cleanLang === 'bs') return 'bs';
  return 'en';
}

function initLanguage() {
  const savedLang = localStorage.getItem('lang');
  if (savedLang) {
    currentLang = savedLang;
  } else {
    currentLang = detectDeviceLanguage();
    localStorage.setItem('lang', currentLang);
  }
  setLanguage(currentLang);
}

function setLanguage(lang) {
  currentLang = lang;
  localStorage.setItem('lang', lang);

  // Update HTML direction and language attributes
  if (lang === 'ar') {
    document.documentElement.setAttribute('dir', 'rtl');
    document.documentElement.setAttribute('lang', 'ar');
  } else {
    document.documentElement.setAttribute('dir', 'ltr');
    document.documentElement.setAttribute('lang', lang);
  }

  // Update DOM elements with data-i18n attributes
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (TRANSLATIONS[lang] && TRANSLATIONS[lang][key]) {
      // Check if it's an input with placeholder
      if (el.tagName === 'INPUT' && el.hasAttribute('placeholder')) {
        el.setAttribute('placeholder', TRANSLATIONS[lang][key]);
      } else {
        el.innerHTML = TRANSLATIONS[lang][key];
      }
    }
  });

  // Sync language select dropdown
  const selector = document.getElementById('langSelect');
  if (selector) selector.value = lang;

  // Refresh active views if app functions are available
  if (typeof renderVocabGrid === 'function' && typeof currentPage !== 'undefined' && currentPage === 'vocabulary') {
    renderVocabGrid();
  }
  if (typeof buildAudioList === 'function' && typeof currentPage !== 'undefined' && currentPage === 'listening') {
    buildAudioList();
  }
  if (typeof populateGrammarPage === 'function' && typeof currentPage !== 'undefined' && currentPage === 'grammar') {
    populateGrammarPage();
  }
  if (typeof buildSidebarChapters === 'function') {
    buildSidebarChapters();
  }
  if (typeof buildChaptersGrid === 'function' && typeof currentPage !== 'undefined' && currentPage === 'dashboard') {
    buildChaptersGrid();
  }
  if (typeof setDailyContent === 'function' && typeof currentPage !== 'undefined' && currentPage === 'dashboard') {
    setDailyContent();
  }
  if (typeof buildWritingPage === 'function' && typeof currentPage !== 'undefined' && currentPage === 'writing') {
    buildWritingPage();
  }
  if (typeof buildConversationSelection === 'function' && typeof currentPage !== 'undefined' && currentPage === 'conversation') {
    buildConversationSelection();
  }
  if (typeof buildQuizSelection === 'function' && typeof currentPage !== 'undefined' && currentPage === 'quiz') {
    buildQuizSelection();
  }
  if (typeof currentPage !== 'undefined' && currentPage === 'lesson' && typeof currentLesson !== 'undefined' && currentLesson) {
    const subtitleEl = document.getElementById('lessonSubtitle');
    if (subtitleEl) {
      subtitleEl.textContent = getChapterTitle(currentLesson) + ' · Kapitel ' + currentLesson.id;
    }
    if (typeof renderLessonContent === 'function') {
      renderLessonContent();
    }
  }

  // Update document title
  const appTitle = getTranslation('app_title', 'Deutsch Lernen A1 | تعلم الألمانية');
  document.title = appTitle;

  // Re-render summarizer if page is active
  if (typeof initSummarizer === 'function' && typeof currentPage !== 'undefined' && currentPage === 'summarizer') {
    initSummarizer();
  }
}

